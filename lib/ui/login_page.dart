  void saveServer() async {
    // obtain shared preferences
    final prefs = await SharedPreferences.getInstance();

// set value
    prefs.setString('server', signupNameController.text);
  }

  void saveUser(data) async {
    // obtain shared preferences
    final prefs = await SharedPreferences.getInstance();

// set value
    prefs.setString('user', data);
  }

  void readServer() async {
    final prefs = await SharedPreferences.getInstance();

// Try reading data from the counter key. If it doesn't exist, return string.
    signupNameController.text =
        prefs.getString('server') ?? "https://ritma.ritmaflex.com";
  }

  Future<http.Response> login() async {
    http
        .post(
      signupNameController.text + '/api/Login/Login/',
      headers: <String, String>{
        'Content-Type': 'application/json; charset=UTF-8',
      },
      body: jsonEncode(<String, String>{
        'email': loginEmailController.text,
        'password': loginPasswordController.text
      }),
    )
        .then((response) {
      saveUser(response.body);
      if (jsonDecode(response.body)["Data"] != null) {
        Navigator.pushReplacementNamed(context, '/choose');
      } else {
        showInSnackBar("Kullanıcı Adı Veya Şifreniz Hatalı");
      }
// Perform the required operation(s)
    });
  }

